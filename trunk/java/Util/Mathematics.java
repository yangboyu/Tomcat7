package Util;

public class Mathematics {
	private int a;
	private int b;
	
	private String c;
	private boolean d;
	
	
	public double add(double a, double b){
		return a + b;
	}


	public int getA() {
		return a;
	}


	public void setA(int a) {
		this.a = a;
	}


	public int getB() {
		return b;
	}


	public void setB(int b) {
		this.b = b;
	}


	public String getC() {
		return c;
	}


	public void setC(String c) {
		this.c = c;
	}


	public boolean isD() {
		return d;
	}


	public void setD(boolean d) {
		this.d = d;
	}
}
